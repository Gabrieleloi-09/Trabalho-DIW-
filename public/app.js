document.addEventListener("DOMContentLoaded", () => {
    // Se existir o elemento container de listagem, estamos na index.html
    const containerLista = document.getElementById("catalogo-itens");
    if (containerLista) {
        renderizarLista(containerLista);
    }

    // Se existir o elemento de detalhes, estamos na detalhes.html
    const containerDetalhe = document.getElementById("detalhe-item");
    if (containerDetalhe) {
        renderizarDetalhes(containerDetalhe);
    }
});

// 1. FUNÇÃO PARA A VITRINE (index.html)
function renderizarLista(container) {
    container.innerHTML = ""; // Limpa o container

    // Acessa o array correto dentro do primeiro app.js (dadosMidia.titulos)
    dadosMidia.titulos.forEach(item => {
        // Criando a estrutura do card do Bootstrap usando as chaves corretas do novo JSON
        const cardHtml = `
            <div class="col-md-4 col-sm-6 mb-4">
                <div class="card h-100 bg-secondary border border-light text-white">
                    <img src="${item.imagem}" class="card-img-top" alt="${item.nome}" style="height: 200px; object-fit: cover;">
                    <div class="card-body d-flex flex-column">
                        <h5 class="card-title fw-bold">${item.nome}</h5>
                        <p class="card-text">${item.descricao_curta}</p>
                        <span class="badge bg-danger mb-2 align-self-start">${item.tipo}</span>
                        <a href="detalhes.html?id=${item.id}" class="btn btn-dark mt-auto">Ver Detalhes</a>
                    </div>
                </div>
            </div>
        `;
        container.innerHTML += cardHtml;
    });
}

// 2. FUNÇÃO PARA OS DETALHES + SUB-ENTIDADES (detalhes.html)
function renderizarDetalhes(container) {
    // Pega o ID enviado na URL (?id=X)
    const urlParams = new URLSearchParams(window.location.search);
    const idParam = parseInt(urlParams.get('id'));

    // Encontra o filme/série correspondente no array correto
    const itemEncontrado = dadosMidia.titulos.find(item => item.id === idParam);

    if (!itemEncontrado) {
        container.innerHTML = `<div class="alert alert-danger">Item não encontrado! <a href="index.html">Voltar</a></div>`;
        return;
    }

    // Primeiro, renderizamos o Layout Personalizado da Entidade Principal (Mínimo 5 itens)
    let htmlResultado = `
        <div class="row m-4">
            <div class="col-md-5 mb-4">
                <img src="${itemEncontrado.imagem}" class="img-fluid rounded border border-light w-100" alt="${itemEncontrado.nome}">
            </div>
            <div class="col-md-7 text-white">
                <span class="badge bg-danger text-uppercase mb-2">${itemEncontrado.tipo}</span>
                <h1 class="display-4 fw-bold">${itemEncontrado.nome}</h1>
                <p class="text-muted">Direção/Autor: <strong>${itemEncontrado.direcao}</strong> | Cadastrado em: ${itemEncontrado.data_cadastro || '2026'}</p>
                <hr class="border-light">
                <p class="lead text-warning">${itemEncontrado.descricao_curta}</p>
                <p>${itemEncontrado.sinopse}</p>
                <p class="small text-info">🎵 Trilha Sonora: ${itemEncontrado.trilha_sonora}</p>
                <a href="index.html" class="btn btn-outline-light mt-3 mb-4">← Voltar para a Vitrine</a>
            </div>
        </div>
        <hr class="border-secondary my-5">
        <div class="row m-4">
            <h3 class="text-white text-uppercase mb-4">Fotos Vinculadas (Personagens do Elenco)</h3>
            <div class="row row-cols-1 row-cols-sm-2 row-cols-md-4 g-4" id="sub-lista-personagens">
    `;

    // Agora, fazemos um laço interno para renderizar a Entidade Secundária (Personagens)
    if (itemEncontrado.personagens && itemEncontrado.personagens.length > 0) {
        itemEncontrado.personagens.forEach(p => {
            htmlResultado += `
                <div class="col">
                    <div class="card h-100 bg-dark text-white text-center border border-secondary">
                        <img src="${p.img}" class="card-img-top" alt="${p.nome}" style="height: 180px; object-fit: cover;">
                        <div class="card-body">
                            <h5 class="card-title text-danger fw-bold">${p.nome}</h5>
                            <p class="card-text small text-muted">${p.bio}</p>
                        </div>
                    </div>
                </div>
            `;
        });
    } else {
        htmlResultado += `<p class="text-muted ps-3">Nenhum personagem vinculado a este título.</p>`;
    }

    // Fecha as divs que abrimos ali em cima
    htmlResultado += `
            </div>
        </div>
    `;

    // Injeta todo o bloco construído de uma vez no container da página
    container.innerHTML = htmlResultado;
}